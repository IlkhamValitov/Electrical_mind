                                <h3>
                                    Что дальше?
                                </h3>

                                <p>
                                    Системы искусственного интеллекта, которые мы только что рассмотрели, являются результатом десятилетий неуклонного развития 
                                    технологий искусственного интеллекта.
                                </p>

                                <p>
                                    На приведенной ниже большой диаграмме эта история за последние восемь десятилетий представлена ​​в перспективе. Он основан на 
                                    наборе данных, подготовленном Хайме Севилья и его коллегами8.
                                </p>

                                <p>
                                    Каждый маленький кружок на этой диаграмме представляет одну систему ИИ. Положение круга на горизонтальной оси указывает, 
                                    когда была построена система ИИ, а его положение на вертикальной оси показывает объем вычислений, который был использован 
                                    для обучения конкретной системы ИИ.
                                </p>

                                <p>
                                    Обучающие вычисления измеряются в операциях с плавающей запятой, или сокращенно FLOP. Один FLOP эквивалентен одному сложению, 
                                    вычитанию, умножению или делению двух десятичных чисел.
                                </p>

                                <p>
                                    Все системы ИИ, которые полагаются на машинное обучение, должны быть обучены, и в этих системах обучающие вычисления являются 
                                    одним из трех фундаментальных факторов, определяющих возможности системы. Двумя другими факторами являются алгоритмы и входные 
                                    данные, используемые для обучения. Визуализация показывает, что по мере увеличения обучающих вычислений системы ИИ становятся 
                                    все более и более мощными.
                                </p>

                                <p>
                                    Хронология восходит к 1940-м годам, к самому началу электронных компьютеров. Первая показанная система искусственного 
                                    интеллекта — «Тесей», роботизированная мышь Клода Шеннона 1950 года, о которой я упоминал в начале. На другом конце временной 
                                    шкалы вы найдете системы искусственного интеллекта, такие как DALL-E и PaLM, чьи способности создавать фотореалистичные 
                                    изображения, а также интерпретировать и генерировать язык, который мы только что видели. Они входят в число систем 
                                    искусственного интеллекта, которые на сегодняшний день использовали наибольший объем обучающих вычислений.
                                </p>

                                <p>
                                    Обучающий расчет построен в логарифмическом масштабе, так что от каждой линии сетки к следующей он показывает 100-кратное 
                                    увеличение. Эта долгосрочная перспектива показывает непрерывный рост. В течение первых шести десятилетий тренировочные 
                                    вычисления увеличивались в соответствии с законом Мура, удваиваясь примерно каждые 20 месяцев. Примерно с 2010 года этот 
                                    экспоненциальный рост еще больше ускорился, и время удвоения составило всего около 6 месяцев. Это поразительно высокие темпы 
                                    роста9.

                                </p>

                                <p>
                                    Быстрое удвоение времени привело к значительному увеличению. Обучающие вычисления PaLM составили 2,5 миллиарда петафлопс, что 
                                    более чем в 5 миллионов раз больше, чем у AlexNet, ИИ с самыми большими обучающими вычислениями всего 10 лет назад.
                                </p>

                                <p>
                                    Масштабирование уже было экспоненциальным и значительно ускорилось за последнее десятилетие. Что мы можем извлечь из этого 
                                    исторического развития для будущего ИИ?
                                </p><br>

