                                <h3>
                                    Изучение долгосрочных тенденций для прогнозирования будущего ИИ
                                </h3>

                                <p>
                                    Исследователи ИИ изучают эти долгосрочные тенденции, чтобы увидеть, что возможно в будущем.
                                </p>

                                <p>
                                    Возможно, наиболее широко обсуждаемое исследование такого рода было опубликовано исследователем ИИ Аджейей Котрой. Она изучила 
                                    увеличение обучающих вычислений, чтобы выяснить, в какой момент времени вычисления для обучения системы ИИ могут соответствовать 
                                    вычислениям человеческого мозга. Идея состоит в том, что на этом этапе система искусственного интеллекта будет соответствовать 
                                    возможностям человеческого мозга. В своем последнем отчете Котра оценила 50-процентную вероятность того, что такой «преобразующий 
                                    ИИ» будет разработан к 2040 году, то есть менее чем через два десятилетия.
                                </p>

                                <p>
                                    В связанной статье я обсуждаю, что преобразующий ИИ будет означать для мира. Короче говоря, идея состоит в том, что такая система 
                                    ИИ будет достаточно мощной, чтобы перенести мир в «качественно другое будущее». Это может привести к изменению в масштабах двух 
                                    более ранних крупных преобразований в истории человечества, сельскохозяйственной и промышленной революций. Это, безусловно, будет 
                                    самым важным глобальным изменением в нашей жизни.
                                </p>

                                <p>
                                    Работа Котры особенно актуальна в этом контексте, поскольку она основывала свой прогноз на исторической долгосрочной тенденции 
                                    обучающих вычислений, которую мы только что изучили. Но стоит отметить, что другие прогнозисты, опирающиеся на другие соображения, 
                                    приходят к в целом аналогичным выводам. Как я показал в своей статье о временных рамках ИИ, многие эксперты в области ИИ считают, 
                                    что существует реальный шанс того, что искусственный интеллект человеческого уровня будет разработан в течение следующих десятилетий, 
                                    а некоторые считают, что он появится гораздо раньше.
                                </p><br>

                                <h3>Создание общедоступного ресурса для обеспечения необходимого публичного обсуждения</h3>

                                <p>
                                    Компьютеры и искусственный интеллект сильно изменили наш мир, но мы все еще находимся на ранних этапах этой истории. Поскольку эта 
                                    технология кажется такой знакомой, легко забыть, что все эти технологии, с которыми мы взаимодействуем, являются совсем недавними 
                                    инновациями и что самые глубокие изменения еще впереди.
                                </p>

                                <p>
                                    Искусственный интеллект уже изменил то, что мы видим, знаем и делаем. И это несмотря на то, что у этой технологии совсем короткая 
                                    история.
                                </p>

                                <p>
                                    Нет никаких признаков того, что эти тенденции достигнут каких-либо пределов в ближайшее время. Наоборот, особенно в течение 
                                    последнего десятилетия, основные тенденции ускорились: инвестиции в технологию искусственного интеллекта быстро увеличились, а 
                                    время удвоения обучающих вычислений сократилось до шести месяцев.
                                </p>

                                <p>
                                    Все крупные технологические инновации приводят к целому ряду положительных и отрицательных последствий. Это уже относится к 
                                    искусственному интеллекту. По мере того, как эта технология становится все более и более мощной, мы должны ожидать, что ее влияние 
                                    станет еще больше.
